function leap_seconds = getLeapSeconds(date_time)
    % get the number of leap seconds
    % 
    % leap_seconds = getLeapSeconds(date_time)
    % 
    % Inputs:
    %   @date_time   = [n*1 datetime] the date and time
    % 
    % Outputs:
    %   @leap_seconds = [n*1 double] the number of leap seconds
    % 
    % ^n = number of datetimes
    % 

    % get the leap dates
    leap_dates = datetime({ ...
        '01/07/1981', ...
        '01/07/1982', ...
        '01/07/1983', ...
        '01/07/1985', ...
        '01/01/1988', ...
        '01/01/1990', ...
        '01/01/1991', ...
        '01/07/1992', ...
        '01/07/1993', ...
        '01/07/1994', ...
        '01/01/1996', ...
        '01/07/1997', ...
        '01/01/1999', ...
        '01/01/2006', ...
        '01/01/2009', ...
        '01/07/2012', ...
        '01/07/2015', ...
        '01/01/2017', ...
        ... % add more leap second days here
    }, 'InputFormat', 'dd/MM/yyyy');

    % get the number of leap seconds
    leap_seconds = sum(juliandate(date_time) >= juliandate(leap_dates), 2);
end